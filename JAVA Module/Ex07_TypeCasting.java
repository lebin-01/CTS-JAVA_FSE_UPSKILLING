public class Ex07_TypeCasting {
    public static void main(String[] args) {
        double d = 9.99;
        int i = (int) d;
        System.out.println("double " + d + " -> int: " + i);

        int x = 7;
        double y = (double) x;
        System.out.println("int " + x + " -> double: " + y);
    }
}
